    protected void doPost(HttpServletRequest request, HttpServletResponse response) throws ServletException, IOException {

        CompagnieModel model = new CompagnieModel();
        model.setNom_compagnie(request.getParameter("nom_compagnie"));
        model.setEmail(request.getParameter("email"));
        model.setAdresse(request.getParameter("adresse"));
        model.setVille(request.getParameter("ville"));
        model.setTelephone(request.getParameter("telephone"));

        if (request.getParameter("mdp").equals(request.getParameter("newmdp"))) {
            model.setMdp(request.getParameter("mdp"));
            model.AjouterCompagnie();
            response.sendRedirect("ajouterCompagnie.jsp?msg=Compagnie ajoutee avec succes!");
        } else {
            response.sendRedirect("ajouterCompagnie.jsp?msg=Les mots de passe ne correspondent pas!");
        }

    }

